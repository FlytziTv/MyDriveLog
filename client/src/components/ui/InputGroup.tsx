import { cn } from "../../lib/utils";
import { ChevronDown } from "lucide-react";

function InputGroup({ children }: { children: React.ReactNode }) {
  return (
    <div className={cn("w-full flex flex-col gap-2 items-start")}>
      {children}
    </div>
  );
}

